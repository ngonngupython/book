# int và float
a_int = 1
print(float(a_int))

a_float = 1.6
print(int(a_float))

# string và number
a_string = "1234"
print(int(a_string))

b_string = "1234.5678"
print(float(b_string))

print(str(123))
print(str(1.23))

# Làm tròn số
print(round(3.141592, 1), round(3.141592, 2), round(3.141592, 30))
